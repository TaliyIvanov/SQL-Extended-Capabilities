/*Немного теории 

Оператор REGEXP позволяет задать регулярное выражение, которому должно соответствовать значение столбца. Синтаксис REGEXP:
строка [NOT] REGEXP регулярное_выражение
Регулярное выражение может содержать текст и специальные символы. Если произвольный текст встречается в регулярном выражении, 
то результатом сравнения будет истина, если этот текст встречается в любом месте строки.

Например:
"я изучаю SQL" REGEXP "SQL" - истина
"я изучаю sql" REGEXP "SQL" - истина
"я изучаю Structure Query Language" REGEXP "SQL" - ложь
На данной платформе по умолчанию установлено, что строка шаблона - регистро-независимая, то есть SQL можно записать как большими, так и маленькими буквами.
Оператор REGEXP по выполняемой функции похож на оператор LIKE, но позволяет реализовать более сложный поиск по строке. 
Так, например, следующие выражения выдают совершенно одинаковый результат:
"я изучаю SQL" REGEXP "SQL" - истина
"я изучаю SQL" LIKE "%SQL%" - истина*/


SELECT applicant, position
FROM resume
WHERE position REGEXP 'менеджер'
ORDER BY applicant;


