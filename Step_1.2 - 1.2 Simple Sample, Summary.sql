строка [NOT] REGEXP регулярное_выражение
Регулярное выражение может содержать текст и специальные символы. 
Если произвольный текст встречается в регулярном выражении, то результатом сравнения будет истина, если этот текст встречается в любом месте строки.
В регулярном выражении можно использовать следующие специальные символы:

^ - указывает на начало строки;
